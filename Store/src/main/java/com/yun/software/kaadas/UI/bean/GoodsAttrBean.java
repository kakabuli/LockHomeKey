package com.yun.software.kaadas.UI.bean;

public class GoodsAttrBean {

    private String logo ;
    private String id ;
    private String realQty ;
    private String price ;
    private String skuName ;
    private String attributeComboName ;
    private String businessId;
    private String name;
    private String oldPrice;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBusinessId() {
        return businessId;
    }

    public void setBusinessId(String businessId) {
        this.businessId = businessId;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRealQty() {
        return realQty;
    }

    public void setRealQty(String realQty) {
        this.realQty = realQty;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getSkuName() {
        return skuName;
    }

    public void setSkuName(String skuName) {
        this.skuName = skuName;
    }

    public String getAttributeComboName() {
        return attributeComboName;
    }

    public void setAttributeComboName(String attributeComboName) {
        this.attributeComboName = attributeComboName;
    }

    public String getOldPrice() {
        return oldPrice;
    }

    public void setOldPrice(String oldPrice) {
        this.oldPrice = oldPrice;
    }
}
